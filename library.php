<script>
    const btn = document.querySelector(".dropdown-btnt");
    const menu = document.querySelector(".dropdown-menut");
    const items = document.querySelectorAll(".dropdown-itemt");

    // toggle
    btn.addEventListener("click", () => {
        menu.style.display = menu.style.display === "block" ? "none" : "block";
    });

    // select
    items.forEach(item => {
        item.addEventListener("click", () => {
            btn.textContent = item.textContent;
            menu.style.display = "none";
        });
    });

    // outside click
    document.addEventListener("click", (e) => {
        if (!e.target.closest(".dropdownt")) {
            menu.style.display = "none";
        }
    });
</script>
<script>
    document.querySelectorAll('.custom-checkboxt').forEach((checkbox) => {
        checkbox.addEventListener('change', function () {
            const text = this.nextElementSibling;
    
            if (this.checked) {
                text.classList.add('active');
            } else {
                text.classList.remove('active');
            }
        });
    });
    </script>
