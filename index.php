<script>
    // ACTIVE CLASS FIX
    document.querySelectorAll('.hero_tab').forEach(btn => {
        btn.addEventListener('click', function () {
            document.querySelectorAll('.hero_tab').forEach(b => b.classList.remove('active'));
            this.classList.add('active');
        });
    });

    // HOVER PLAY
    document.querySelectorAll('.hero_tab video').forEach(video => {
        video.addEventListener('mouseenter', () => video.play());
        video.addEventListener('mouseleave', () => {
            video.pause();
            video.currentTime = 0;
        });
    });
</script>